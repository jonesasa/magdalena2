package com.almi.dex7cdr7y876767767.datos;

public class Fotos {

    private String fotos;
    private String texto;

    public Fotos(String fotos, String texto) {
        this.fotos = fotos;
        this.texto = texto;
    }

    public String getFotos() {
        return fotos;
    }

    public void setFotos(String fotos) {
        this.fotos = fotos;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }
}
